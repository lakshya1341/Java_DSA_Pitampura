package Week0.Day2.Loops;

public class Whileloop {
    public static void main(String[] args) {
        //While_loop
        
        System.out.println("While_loop");
        int i=1;
        while(i<=10){
            System.out.println(i);
            i++;
        }

        //Do_while_Loop
        System.out.println("Do_while_loop");
        int a=9;
        do{
            System.out.println(a);
            a++;
        }while(a<=10);
        System.out.println(" ");
    }
}
